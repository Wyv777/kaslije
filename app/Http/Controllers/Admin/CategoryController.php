<?php

namespace App\Http\Controllers\Admin;


use App\Models\Category;
use Illuminate\Http\Request;
use App\Http\Controllers\Controller;

class CategoryController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $categories = Category::with('childrenRecursive')->where('parent_id',1)->get();
        return view('admin.category.index',['categories' => $categories]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $categories = Category::select('id','name','is_active','parent_id','slug')->get();
        return view('admin.category.create',['categories' => $categories]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => 'required',
            'is_active' => 'required',
            'parent_id' => 'required',
            'slug'=> 'required',
        ]);

        $category = new Category;
        $category->name = $request->name;
        $category->is_active = $request->is_active;
        $category->parent_id = $request->parent_id;
        $category->slug = $request->slug;
        $category->save();

        return redirect('/admin/categories');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $category = Category::find($id);
        $category->parent_name = $category->parent->name;
        return view('admin.category.show',['category'=>$category]);
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $categories = Category::select('id','name','is_active','parent_id','slug')->get();
        $category = Category::find($id);
        $category->parent_name = $category->parent->name;
        return view('admin.category.edit',['category' => $category, 'categories' => $categories]);
    }

